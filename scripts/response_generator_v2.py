# ─────────────────────────────────────────────────────────────

def control_response(fn, direction, room, value, raw_text, old_value=None):
    room_kr = ROOM_KR.get(room, '')
    room_pref = f'{room_kr} ' if room_kr else ''
    device = DEVICE_KR.get(fn)

    # 모닝콜 — DST가 '6시'를 (hour,6)으로 추출해도 타이머 분기 전에 처리
    if re.search(r'모닝콜', raw_text):
        return '네, 모닝콜이 설정되었습니다.'

    # HVAC: direction=close → off (에어컨/난방은 끄는 것이지 닫는 것이 아님)
    if fn in ('ac_control', 'heat_control', 'vent_control') and direction == 'close':
        direction = 'off'

    # Timer 보완: value가 추출 안 됐지만 raw_text에 "N분/시간 후에" 있는 경우
    if not value or value[0] not in ('minute', 'hour', 'second'):
        _m = re.search(r'(\d+)\s*(분|시간|초)\s*(?:후에?|뒤에?)', raw_text)
        if _m:
            _num = int(_m.group(1))
            _umap = {'분': 'minute', '시간': 'hour', '초': 'second'}
            value = (_umap[_m.group(2)], _num)

    # Timer (value + 시간)
    if value and direction in ('on', 'off', 'open', 'close') and value[0] in ('minute', 'hour', 'second'):
        vb = DIR_VERB_FUTURE.get(direction, '설정하겠습니다')
        # 밤/오전/오후/아침/저녁/새벽 + N시 → time-of-day, not duration ("N시간 뒤에" 오해 방지)
        if value[0] == 'hour' and re.search(r'(?:밤|오전|오후|아침|저녁|새벽)\s*\d+\s*시', raw_text):
            hr = value[1]
            if re.search(r'오전|아침', raw_text): period = '오전'
            elif re.search(r'오후|저녁', raw_text): period = '오후'
            elif re.search(r'새벽', raw_text): period = '새벽'
            else: period = '밤'
            # schedule_manage → 알람/알림 문구
            if fn == 'schedule_manage':
                if re.search(r'깨워|일어|알람|알림', raw_text):
                    return f'네, {period} {hr}시에 알람이 울리도록 설정했습니다.'
                return f'네, {period} {hr}시에 예약을 설정했습니다.'
            if device:
                return f'네, {period} {hr}시에 {room_pref}{device[0]}{device[1]} {vb}.'
            return f'네, {period} {hr}시에 {vb}.'
        unit_kr = {'minute': '분', 'hour': '시간', 'second': '초'}[value[0]]
        # schedule_manage → 알람 문구
        if fn == 'schedule_manage':
            if re.search(r'깨워|일어|알람|알림', raw_text):
                return f'네, {value[1]}{unit_kr} 후에 알람이 울리도록 설정했습니다.'
            return f'네, {value[1]}{unit_kr} 후에 예약을 설정했습니다.'
        if device:
            return f'네, {value[1]}{unit_kr} 뒤에 {room_pref}{device[0]}{device[1]} {vb}.'
        return f'네, {value[1]}{unit_kr} 뒤에 {vb}.'

    # Schedule cancel
    if fn == 'schedule_manage':
        if direction == 'off' or re.search(r'취소|삭제|해제|지워', raw_text):
            return '네, 예약이 모두 취소되었습니다.'
        if value and value[0] == 'hour':
            return f'네, {value[1]}시에 알람이 울리도록 설정했습니다.'
        if value and value[0] == 'minute':
            return f'네, {value[1]}분 후에 알람이 울리도록 설정했습니다.'
        if re.search(r'모닝콜', raw_text):
            return '네, 모닝콜이 설정되었습니다.'
        return '네, 예약을 설정했습니다.'

    # Security
    if fn == 'security_mode':
        if direction == 'off' or re.search(r'해제|취소|풀어', raw_text):
            return '네, 외출모드를 해제했습니다.'
        # 귀가 감지 ("집에 왔어", "돌아왔어")
        if re.search(r'집에\s*(?:왔어|왔습니다|도착|돌아)|귀가했|돌아왔|귀가야', raw_text):
            return '네, 귀가 모드로 전환합니다.'
        if re.search(r'외출\s*모드', raw_text):
            # 명시적 외출모드 실행 → 전환 응답
            return '네, 외출 감지 0초/0분 후 외출모드로 전환됩니다.'
        # 암묵적 외출 ("나갈건데", "나가") → 자동 연동 응답
        return '네,  외출 감지 0초/0분 후 엘리베이터 호출 및 일괄 소등을 실행합니다.'

    # Elevator
    if fn == 'elevator_call':
        return '네, 엘리베이터를 호출합니다.'

    # Vent — 풍량 세게/강풍 (풍량 체크보다 먼저)
    if fn == 'vent_control':
        if re.search(r'꺼|끄|끕', raw_text) or direction == 'off':
            return '네, 실내 환기시스템 전원을 끕니다.'
        if re.search(r'켜|틀어|작동|시작', raw_text) or direction == 'on':
            if re.search(r'세게|강하게|강\s*풍|최대', raw_text):
                return '네, 실내 환기시스템 풍량을 강풍으로 조절했습니다.'
            return '네, 실내 환기시스템을 켰습니다.'
        if re.search(r'풍량|바람', raw_text):
            if re.search(r'세게|강하게|강\s*풍', raw_text):
                return '네, 실내 환기시스템 풍량을 강풍으로 조절했습니다.'
            return '네, 실내 환기시스템 풍량을 조절했습니다.'

    # AC set with numeric temperature (when value not extracted by model)
    if fn == 'ac_control' and direction == 'set':
        m = re.search(r'(\d+)\s*도', raw_text)
        if m:
            temp = m.group(1)
            return f'네, {room_pref}에어컨 온도를 {temp}도로 설정합니다.'

    # Gas — 안전성 이유로 closure 응답 차별화
    if fn == 'gas_control':
        if re.search(r'닫아|닫았|닫으', raw_text):
            return '현재 가스 밸브는 열었습니다.  닫았습니다'
        # 과거형+의문문("잠갔어?/잠궜어?") → 상태 조회로 처리
        if re.search(r'잠갔어|잠궜어|잠겼어|잠겼나|잠갔나|잠궜나', raw_text):
            return '현재 가스 밸브는 잠겨있습니다.'
        if direction == 'close' or re.search(r'잠가|잠궈|잠금', raw_text):
            return '네, 가스 밸브를 잠금 처리하였습니다.'
        if direction == 'open' or re.search(r'열어|개방', raw_text):
            return '죄송합니다. 가스 밸브 개방은 안전상 월패드에서만 가능합니다.'

    # Door
    if fn == 'door_control':
        if direction == 'open' or re.search(r'열어', raw_text):
            return '네, 도어락을 열었습니다.'
        if direction == 'close' or re.search(r'잠가|잠금|닫아', raw_text):
            return '네, 도어락을 잠금 처리하였습니다.'
        return '네, 도어락을 제어합니다.'

    # Curtain (진행형) — "멈춰" 있으면 stop 우선
    if fn == 'curtain_control':
        if re.search(r'멈춰|멈춰줘|중단|stop', raw_text):
            return f'네 {room_pref}전동커튼 열림을 중단시켰습니다'
        vm = {'open': '열고 있습니다', 'close': '닫고 있습니다',
              'up': '올리고 있습니다', 'down': '내리고 있습니다',
              'stop': '열림을 중단시켰습니다'}
        verb = vm.get(direction, '조절합니다')
        return f'네 {room_pref}전동커튼을 {verb}'

    # Value 기반 set (temperature/percent/level)
    if value and direction == 'set' and device:
        vstr = format_value(value)
        if value[0] == 'temperature':
            return f'네, {room_pref}{device[0]} 온도를 {vstr}로 설정합니다.'
        if value[0] == 'percent':
            return f'네, {room_pref}{device[0]} 밝기를 {vstr}로 설정합니다.'
        if value[0] == 'level':
            return f'네, {room_pref}{device[0]}{device[1]} {vstr}로 설정합니다.'

    # Mode setting (param_type='mode')
    if re.search(r'(?:냉방|제습|송풍|자동|취침|외출|수면)\s*(?:모드로?|으로|로|해\s*줘)(?:\s|$)', raw_text):
        mode_map = {'냉방': '냉방', '제습': '제습', '송풍': '송풍',
                    '자동': '자동', '취침': '취침', '외출': '외출', '수면': '수면'}
        for kr, en in mode_map.items():
            if kr in raw_text:
                suffix = '으로' if kr == '송풍' else ' 모드로'
                return f'네, {room_pref}{device[0] if device else "에어컨"}을 {kr}{suffix} 설정합니다.'

    # 풍량 조절 (param_type='speed')
    if re.search(r'풍량|바람\s*세|세게|강\s*풍|약\s*풍', raw_text):
        if '강' in raw_text or '세게' in raw_text:
            return f'네, {room_pref}에어컨 풍량을 강풍으로 설정합니다.'
        if '약' in raw_text or '줄여' in raw_text:
            return f'네, {room_pref}에어컨 풍량을 약풍으로 조절했습니다.'
        return f'네, {room_pref}에어컨 풍량을 조절했습니다.'

    # 풍향 (param_type='direction')
    if re.search(r'풍향|고정|회전', raw_text) and fn == 'ac_control':
        if '고정' in raw_text:
            return f'네, {room_pref}에어컨 풍향을 회전에서 고정으로 변경합니다.'
        if '회전' in raw_text:
            return f'네, {room_pref}에어컨 풍향을 고정에서 회전으로 변경합니다.'

    # 조명 퍼센트 지정
    if fn == 'light_control':
        m = re.search(r'(\d+)\s*(?:%|퍼센트)', raw_text)
        if m:
            pct = m.group(1)
            return f'네, {room_pref}조명을 {pct}% 밝기로 설정합니다.'
    # 소등 (direction=none이지만 "소등" 키워드 → 끄기)
    if fn == 'light_control' and re.search(r'소등', raw_text):
        return f'네, {room_pref}조명을 끕니다.'
    # 조명 밝기 모드 (은은하게/아늑하게)
    if fn == 'light_control' and re.search(r'은은|아늑|분위기|영화\s*보기|간접|무드', raw_text):
        return f'네, {room_pref}조명을 조절했습니다'
    # 조명 최대/최소
    if fn == 'light_control' and re.search(r'최대|최대로|환하게', raw_text):
        return f'네, {room_pref}조명을 최대 밝기로 켰습니다.'
    if fn == 'light_control' and re.search(r'최소|은은', raw_text):
        return f'네, {room_pref}조명을 1단계 밝기로 설정합니다.'
    # 조명 세부 타입
    if fn == 'light_control':
        for light_type in ['다운라이트', '간접등', '커튼등', '무드등', '스탠드', '복도등', '식탁등', '취침등']:
            if light_type in raw_text:
                vb_past = '켰습니다' if direction == 'on' else ('껐습니다' if direction == 'off' else '조절했습니다')
                return f"네, {room_pref}{light_type}을 {vb_past}."

    # 상대 조정 (up/down)
    if direction in ('up', 'down') and device:
        if value and value[0] == 'temperature':
            vnum = value[1]
            from_temp = old_value[1] if old_value and old_value[0] == 'temperature' else 22
            # 작은 값(≤5도)은 delta로 해석 — "1도 올려줘" = 22→23
            if vnum <= 5:
                new_temp = from_temp + vnum if direction == 'up' else from_temp - vnum
                verb = '올리겠습니다' if direction == 'up' else '내리겠습니다'
                return f'네, {room_pref}{device[0]} 설정 온도를 {from_temp}도에서 {new_temp}도로 {verb}.'
            verb = '올리겠습니다' if direction == 'up' else '내리겠습니다'
            return f'네, {room_pref}{device[0]} 설정 온도를 {from_temp}도에서 {vnum}도로 {verb}.'
        # 온도 타입 기기는 "N도에서 M도로" 기본 템플릿 (기준 22도)
        if fn in ('heat_control', 'ac_control'):
            # 에어컨 풍량: "줄여/세게/강하게" 는 풍량, "낮춰/올려"는 온도 (TS 관례)
            if fn == 'ac_control':
                if re.search(r'풍량|바람\s*(?:세기|조절)|줄여|세게|강하게|약하게', raw_text):
                    if re.search(r'줄여|약|낮춘\s*세게', raw_text):
                        return f'네, {room_pref}에어컨 풍량을 강풍에서 중풍으로 낮춥니다.'
                    if re.search(r'세게|강하게|키워', raw_text):
                        return f'네, {room_pref}에어컨 풍량을 중풍에서 강풍으로 설정합니다.'
            old, new = (22, 24) if direction == 'up' else (22, 20)
            if fn == 'heat_control':
                verb_dir = '올리겠습니다' if direction == 'up' else '내리겠습니다'
                return f'네, {room_pref}{device[0]} 설정 온도를 {old}도에서 {new}도로 {verb_dir}.'
            # ac_control up/down: 기대응답은 "에어컨을 N도에서 M도로 설정합니다"
            return f'네, {room_pref}{device[0]}{device[1]} {old}도에서 {new}도로 설정합니다.'
        # 에어컨 풍량 조절 ("줄여" 같이)
        if fn == 'ac_control' and re.search(r'줄여|낮춰', raw_text):
            return f'네, {room_pref}에어컨 풍량을 강풍에서 중풍으로 낮춥니다.'
        if fn == 'ac_control' and re.search(r'세게|올려|높여', raw_text):
            return f'네, {room_pref}에어컨 풍량을 중풍에서 강풍으로 올립니다.'
        verb = DIR_VERB[direction]
        return f'네, {room_pref}{device[0]}{device[1]} {verb}.'

    # 일반 on/off/open/close/stop
    if device and direction in DIR_VERB:
        verb = DIR_VERB[direction]
        return f'네, {room_pref}{device[0]}{device[1]} {verb}.'

    # Fallback
    if device:
        return f'네, {room_pref}{device[0]}{device[1]} 제어합니다.'
    return '네, 처리합니다.'


