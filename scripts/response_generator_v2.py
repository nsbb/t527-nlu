def format_value(value):
    if not value:
        return None
    vtype, vnum = value
    unit = {'temperature': '도', 'minute': '분', 'hour': '시간', 'second': '초',
            'percent': '%', 'level': '단계'}.get(vtype, '')
    return f'{vnum}{unit}'


