    text = ''.join(c if c.isprintable() or c == ' ' else ' ' for c in text)

    # 2. 공백 정리
    text = re.sub(r'\s+', ' ', text).strip()

    # 2-2. iter10: 글자 사이 단일 공백 collapse ("가 스 잠 가" → "가스잠가")
    # continuous: 긴 multi-char 토큰 (2글자 이상)이 있으면 collapse 안 함
    # (예: "켜 줘 거실 불 좀" — 거실이 2글자이므로 유지)
    tokens = text.split(' ')
    single_char_count = sum(1 for t in tokens if len(t) == 1 and '가' <= t <= '힣')
    long_token_count = sum(1 for t in tokens if len(t) >= 2 and all('가' <= c <= '힣' for c in t))
    if single_char_count >= 4 and long_token_count == 0 and single_char_count / max(len(tokens), 1) >= 0.9:
        # 모든 토큰이 단일 글자 — 공백 제거
        text = ''.join(tokens)

    # 3. STT 오류 사전 교정 — 두 번 pass
    #    첫 pass: 긴 패턴 (prefix 겹침 방지, "지금몇시야" > "지금몇시")
    #    둘째 pass: 첫 pass 후 활성화된 composite 매칭 ("까스잠가" → "가스잠가" → "가스 잠가")
    # iter10: '라이트' 같은 substring 겹침 문제 해결 — placeholder 사용
    placeholders = {}
    for special in ('다운라이트', '간접등', '취침등', '무드등'):
        if special in text:
            ph = f'__PH{len(placeholders)}__'
            text = text.replace(special, ph)
            placeholders[ph] = special

    for _ in range(2):
        for wrong, correct in sorted(STT_CORRECTION.items(), key=lambda kv: -len(kv[0])):
            text = text.replace(wrong, correct)

    # Restore placeholders
    for ph, original in placeholders.items():
        text = text.replace(ph, original)

    # 4. 한글 숫자 변환
    text = kr_num_to_arabic(text)

    # 5. 불필요 부사 제거 (STT 결과에 자주 나오는 구어 부사)
    text = re.sub(r'^(잠깐|얼른)\s+', '', text)

    # 5-2. iter10: filler words 제거 (음/아/어/그/저 + ...)
    text = re.sub(r'^(?:음|아|어|그|저|그|그래서)\.{0,3}\s+', '', text)
    text = re.sub(r'\.{2,}', ' ', text)  # "..." → space
    text = re.sub(r'\s+', ' ', text).strip()

    return text


