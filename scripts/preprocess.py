def kr_num_to_arabic(text):
    """한글 숫자 → 아라비아 숫자 변환 (온도/시간 문맥)"""
    # "이십삼도" → "23도"
    m = re.search(r'(이십|삼십|사십|십)([일이삼사오육칠팔구])?도', text)
    if m:
        tens = {'십': 10, '이십': 20, '삼십': 30, '사십': 40}
        ones = {'일': 1, '이': 2, '삼': 3, '사': 4, '오': 5, '육': 6, '칠': 7, '팔': 8, '구': 9}
        num = tens.get(m.group(1), 10) + ones.get(m.group(2), 0)
        text = text[:m.start()] + f'{num}도' + text[m.end():]

    # "스물다섯도" → "25도"
    m = re.search(r'(스물|서른|마흔|쉰)([하나둘셋넷다섯여섯일곱여덟아홉])?도', text)
    if m:
        tens = {'스물': 20, '서른': 30, '마흔': 40, '쉰': 50}
        ones = {'하나': 1, '둘': 2, '셋': 3, '넷': 4, '다섯': 5, '여섯': 6, '일곱': 7, '여덟': 8, '아홉': 9}
        num = tens.get(m.group(1), 20) + ones.get(m.group(2), 0)
        text = text[:m.start()] + f'{num}도' + text[m.end():]

    return text


