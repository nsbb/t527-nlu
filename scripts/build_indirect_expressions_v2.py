from collections import Counter
fn_dist = Counter(x['labels']['fn'] for x in deduped)
dir_dist = Counter(x['labels']['param_direction'] for x in deduped)

print(f"총 {len(deduped)}개 간접 표현")
print(f"\nfn 분포:")
for k, v in fn_dist.most_common():
    print(f"  {k}: {v}")
print(f"\ndirection 분포:")
for k, v in dir_dist.most_common():
    print(f"  {k}: {v}")

out_path = 'data/indirect_expressions_v2.json'
with open(out_path, 'w', encoding='utf-8') as f:
    json.dump(deduped, f, ensure_ascii=False, indent=2)
print(f"\n저장: {out_path}")
